DROP TABLE IF EXISTS LOG; -- 로그
DROP TABLE IF EXISTS TRADE; -- 금액
DROP TABLE IF EXISTS STOCK; -- 종목


CREATE TABLE STOCK (
    STOCK_SEQ BIGINT AUTO_INCREMENT PRIMARY KEY NOT NULL, -- 종목코드
    STOCK_NAME VARCHAR(100) NOT NULL, -- 종목명
    CREATE_USER_ID VARCHAR(50) NOT NULL, -- 생성 사용자 ID
    CREATE_DATE DATETIME NOT NULL, -- 생성 일시
    MODIFY_USER_ID VARCHAR(50) NOT NULL, -- 수정 사용자 ID
    MODIFY_DATE DATETIME NOT NULL -- 수정 일시

);

-- 주식의 수량은 1개로 가정
CREATE TABLE TRADE (
    TRADE_SEQ BIGINT AUTO_INCREMENT PRIMARY KEY NOT NULL, -- 거래 순번
 	STOCK_SEQ BIGINT NOT NULL, -- 종목코드
 	TRADE_TYPE VARCHAR(3) NOT NULL, -- 매수/매도
 	OLD_PRICE INT NOT NULL, -- 기존 금액
 	TRADE_PRICE INT NOT NULL, -- 매수/매도 금액
 	TRADE_DAY VARCHAR(8) NOT NULL, -- 일자
 	TRADE_TIME VARCHAR(4) NOT NULL, -- 일시
    CREATE_USER_ID VARCHAR(50) NOT NULL, -- 생성 사용자 ID
    CREATE_DATE DATETIME NOT NULL -- 생성 일시
);

CREATE TABLE LOG (
    LOG_SEQ BIGINT AUTO_INCREMENT PRIMARY KEY NOT NULL, -- 로그 순번
 	STOCK_SEQ BIGINT NOT NULL, -- 종목코드
 	BEGIN_PRICE INT NULL, -- 시작금액
 	END_PRICE INT NULL, -- 시작금액
 	VIEW_CNT INT  NULL, -- 조회수
 	TRADE_CNT INT  NULL, -- 조회수
 	LOG_DAY VARCHAR(8) NOT NULL, -- 일자
    CREATE_USER_ID VARCHAR(50) NOT NULL, -- 생성 사용자 ID
    CREATE_DATE DATETIME NOT NULL, -- 생성 일시
    MODIFY_USER_ID VARCHAR(50) NOT NULL, -- 수정 사용자 ID
    MODIFY_DATE DATETIME NOT NULL -- 수정 일시
);


